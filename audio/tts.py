from gtts import gTTS

# 대본 텍스트
script_text = """
여러분, 오늘 회의는 새 소프트웨어 개발 프로젝트의 현재 진행 상황을 점검하기 위해 모였습니다. 각 팀원의 진행 상황과 문제가 있는 부분을 공유해 주세요. 이 개발자님, 먼저 프론트엔드 개발 상황부터 이야기해 주시겠어요?

네, 현재 로그인 및 회원가입 페이지의 UI는 거의 완성되었습니다. 하지만 사용자 입력 검증 부분에서 약간의 버그가 발생했습니다. 이 부분은 오늘 내로 수정할 예정입니다. 다음 주에는 대시보드 화면 작업을 시작할 계획입니다.

좋습니다. 사용자 입력 검증 부분이 중요한데, 수정하는 데 어려움이 있으면 바로 공유해 주세요. 박 개발자님, 백엔드 쪽 상황은 어떤가요?

현재 데이터베이스 설계와 기본적인 API는 완료되었습니다. 로그인 기능도 테스트를 마쳤고, 정상 작동을 확인했습니다. 다만, 데이터베이스 연결 속도가 다소 느린 문제가 있어 최적화 작업을 진행 중입니다.

알겠습니다. 최적화 작업이 중요한 만큼, 시간을 충분히 투자해 주세요. 최 디자이너님, UI/UX 디자인 진행 상황은 어떻습니까?

로그인 및 회원가입 페이지의 디자인이 완료되어 이 개발자님께 전달드렸습니다. 현재는 대시보드와 프로필 페이지 디자인 작업을 진행 중입니다. 이번 주 안으로 시안을 공유드릴 수 있을 것 같습니다.

네, 디자인이 완성되면 바로 개발팀과 공유해 주세요. 마지막으로 정 QA 엔지니어님, 품질 관리 측면에서 진행 상황을 알려주시겠어요?

현재까지 프론트엔드와 백엔드 기능별 테스트 계획을 수립했고, 기본적인 기능 테스트를 시작했습니다. 이번 주 중으로 로그인 및 회원가입 기능의 테스트를 마무리할 예정입니다. 주요 버그는 아직 발견되지 않았습니다.

좋습니다. 여러분 모두 훌륭하게 작업을 진행해 주셔서 감사합니다. 각자 진행하는 작업에서 어려움이 생기면 바로 공유해 주세요. 다음 주에도 진행 상황 점검을 위한 회의를 진행할 예정이니, 준비 부탁드립니다. 추가로 논의할 사항이 있으면 말씀해 주세요.

하나만 더 말씀드리면, API 문서화 작업을 어떻게 진행할지 논의가 필요할 것 같습니다. 모든 팀원이 참고할 수 있도록 체계적으로 작성하는 것이 중요할 것 같습니다.

좋은 지적입니다. API 문서화는 개발이 완료되는 대로 바로 진행하도록 합시다. 문서화 담당자를 지정하고, 문서 작성 가이드라인을 마련해 공유하도록 하겠습니다. 다른 의견이 없으면 오늘 회의는 여기서 마치겠습니다. 모두 수고하셨습니다!

수고하셨습니다!
"""

# 언어 설정 (한국어)
language = 'ko'

# gTTS 객체 생성
tts = gTTS(text=script_text, lang=language, slow=False)

# 음성 파일로 저장
tts.save("meeting_script.wav")

print("음성 파일이 생성되었습니다.")
